
// 1> Assignment Operators
let a = 87;
let b = 90;


// 2> Arithmetic Operators
console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);

// 3> Comparison Operators
console.log(a < b);
console.log(a <= b);
console.log(a > b);
console.log(a >= b);

// 4> Logical Operators
let c = true;
let d = false;
console.log(c & d);
console.log(c | d);
console.log(!c);
console.log(!d);

// ************************ Short Circuit***********************
console.log(c && d);
console.log(c || d);