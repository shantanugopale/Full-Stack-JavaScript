

// 5 Types of Loops in JS
    // 1> While
    // 2> Do-While
    // 3> For
    // 4> For in
    // 5> For of

// *****************While Loop****************
let i=0;
while(i<10)
{
    console.log(i);
    i++;
}

// *****************Do-While Loop**************
let a=0;
do
{
    console.log(a);
    a++;
}while(a<10);

// *****************For Loop**************

for (let i = 0; i <= 10; i++) {
   
    console.log(i);
}

for (let i = 1; i <= 10; i++) {
   
    console.log(`${i} * ${i} = `+ (i * i));
}