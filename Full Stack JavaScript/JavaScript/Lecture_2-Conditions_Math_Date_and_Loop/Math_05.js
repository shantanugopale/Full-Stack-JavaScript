// Math

const PI = Math.PI;
console.log(PI);
console.log(Math.PI);

// ---------round-ceil-floor--------
console.log(Math.round(PI));
console.log(Math.round(9.7));
console.log(Math.ceil(9.7));
console.log(Math.floor(9.7));

// ----------Min and Max-------------
console.log(Math.min(5,4,3,1));
console.log(Math.max(5,6,7,8,9,10));

// ---------Round--------------------
console.log(Math.random()*50); // 0 to 0.9999 (0-1)
console.log(Math.random()*51);

// ---------Pow----------------------
console.log(Math.pow(3,3));

// ---------exponent-and-log---------
console.log(Math.exp(3));
console.log(Math.log(3));

// ---------sin-cos-tan--------------
console.log(Math.sin(90));
console.log(Math.cos(1));
console.log(Math.tan(45));